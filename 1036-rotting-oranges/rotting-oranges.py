class Solution:
    def orangesRotting(self, grid: List[List[int]]) -> int:
        m,n=len(grid),len(grid[0])
        start=[]
        fresh=0
        for r in range(m):
            for c in range(n):
                if grid[r][c]==2:
                    start.append((r,c))
                elif grid[r][c]==1:
                    fresh+=1
        if fresh==0:
            return 0
        q=deque(start)
        dirs=[(1,0),(-1,0),(0,1),(0,-1)]
        steps=0
        while q and fresh>0:
            for _ in range(len(q)):
                r,c=q.popleft()
                for dr,dc in dirs:
                    nr,nc=r+dr,c+dc
                    if 0<=nr<m and 0<=nc<n and grid[nr][nc]==1:
                        fresh-=1
                        grid[nr][nc]=2
                        q.append((nr,nc))
            steps+=1
        return steps if fresh==0 else -1